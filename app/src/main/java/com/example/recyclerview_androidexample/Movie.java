package com.example.recyclerview_androidexample;

public class Movie {
    private String title11;
    private String gender11;
    private String year11;

    public Movie(String s, String s1, String s2) {
        this.gender11=s;
        this.title11=s1;
        this.year11= s2;
    }

    public String getTitle11() {
        return title11;
    }

    public String getGender11() {
        return gender11;
    }

    public String getYear11() {
        return year11;
    }



    public void setTitle11(String title11) {
        this.title11 = title11;
    }

    public void setGender11(String gender11) {
        this.gender11 = gender11;
    }

    public void setYear11(String year11) {
        this.year11 = year11;
    }






}
